function N = length(G)
% Find the length of a GraphSig object (i.e., the number of vertices in the
% graph)
%
% Input
%   G       a GraphSig object
%
% Output
%   N       the length of the GraphSig object
%
%
%
% Copyright 2015 The Regents of the University of California
%
% Implemented by Jeff Irion (Adviser: Dr. Naoki Saito)



N = G.length;


end